        public void NullSourceThrowsException()
        {
            var converter = new SpacingReader();
            _ = Assert.Throws<ArgumentNullException>(() => converter.Convert(null));
        }

        [Test]
        public void UninitializedConverterThrowsException()
        {
            var ds = DataStreamFactory.FromArray(_data, 0, _data.Length);
            BinaryFormat source = new BinaryFormat(ds);

            var converter = new SpacingReader();
            _ = Assert.Throws<InvalidOperationException>(() => converter.Convert(source));
        }

        [Test]
        public void BadOffsetThrowsException()
        {
            var ds = DataStreamFactory.FromArray(_data, 0, _data.Length);
            BinaryFormat source = new BinaryFormat(ds);
            var parameters = new ReaderParameters { Offset = 6200, Endianness = Endianness.LittleEndian, };

            var converter = new SpacingReader();
            converter.Initialize(parameters);
            _ = Assert.Throws<InvalidOperationException>(() => converter.Convert(source));
        }

        [Test]
        public void WorksWithValidTable()
        {
            var ds = DataStreamFactory.FromArray(_data, 0, _data.Length);
            BinaryFormat binary = new BinaryFormat(ds);

            var parameters = new ReaderParameters { Offset = 0, Endianness = Endianness.LittleEndian, };

            var converter = new SpacingReader();
            converter.Initialize(parameters);
            var result = converter.Convert(binary);

            Assert.IsInstanceOf<CharacterSpacingTable>(result);
            Assert.AreEqual(0, result.TableOffset);
            Assert.AreEqual(0.7f, result[0x27].TopLeft);
        }
